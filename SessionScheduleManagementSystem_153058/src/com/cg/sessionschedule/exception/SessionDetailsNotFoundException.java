package com.cg.sessionschedule.exception;

public class SessionDetailsNotFoundException extends RuntimeException {

	public SessionDetailsNotFoundException() {
		super();
	
	}

	public SessionDetailsNotFoundException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public SessionDetailsNotFoundException(String message, Throwable cause) {
		super(message, cause);
	
	}

	public SessionDetailsNotFoundException(String message) {
		super(message);
	
	}

	public SessionDetailsNotFoundException(Throwable cause) {
		super(cause);
		
	}

	
	
}
